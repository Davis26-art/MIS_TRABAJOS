// Determina si un número es par o impar usando % (módulo).

let numero = 7;

if (numero % 2 === 0) { // Si el resto de dividir por 2 es 0, es par
    console.log(numero, "es PAR");
} else { // Si no, es impar
    console.log(numero, "es IMPAR");
}