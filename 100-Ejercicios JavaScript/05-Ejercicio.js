// Convierte Celsius ↔ Fahrenheit

let celsius = 30;
let fahrenheit = (celsius * 9/5) + 32; // Fórmula para convertir Celsius a Fahrenheit
console.log(celsius, "°C =", fahrenheit, "°F"); 

let f = 86;
let c = (f - 32) * 5/9; // Fórmula para convertir Fahrenheit a Celsius
console.log(f, "°F =", c, "°C");