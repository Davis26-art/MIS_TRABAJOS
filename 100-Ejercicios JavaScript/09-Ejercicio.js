// Determina si un número es positivo, negativo o cero.

let n = -3;

if (n > 0) { // Si 'n' es mayor que 0
    console.log(n, "es POSITIVO");
} else if (n < 0) { // Si 'n' es menor que 0
    console.log(n, "es NEGATIVO");
} else { // Si no, es cero
    console.log("El número es CERO");
}