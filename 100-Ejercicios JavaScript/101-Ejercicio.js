// Encuentra el mayor con operador ternario.

let a = 8, b = 12;                // Definimos dos números a comparar
let mayor = (a > b) ? a : b;       // Operador ternario: si a > b, mayor = a; si no, mayor = b
console.log("Mayor:", mayor);      // Imprime el valor mayor
