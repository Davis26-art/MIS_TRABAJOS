let num = 7;                                      // Definimos el número a evaluar
let resultado = (num % 2 === 0) ? "Par" : "Impar"; // Operador ternario: si el residuo al dividir entre 2 es 0 → Par, si no → Impar
console.log(num, "es", resultado);               // Imprime el resultado, por ejemplo: "7 es Impar"
