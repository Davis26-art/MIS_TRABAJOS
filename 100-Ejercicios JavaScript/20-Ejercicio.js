// Capital inicial
let capital = 1000;

// Tasa de interés (% anual)
let tasa = 5;

// Tiempo en años
let tiempoAnios = 2;

// Fórmula: I = (Capital * Tasa * Tiempo) / 100
let interes = (capital * tasa * tiempoAnios) / 100;

// Imprime el interés calculado
console.log("Interés simple:", interes);
