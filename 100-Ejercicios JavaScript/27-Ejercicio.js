// Número de opción elegida
let opcion = 2;

// Switch según la opción
switch (opcion) {
    case 1:                        // opción 1
        console.log("Has pedido una Pizza");
        break;
    case 2:                        // opción 2
        console.log("Has pedido una Hamburguesa");
        break;
    case 3:                        // opción 3
        console.log("Has pedido una Ensalada");
        break;
    default:                       // cualquier otro número
        console.log("Opción no válida");
}
