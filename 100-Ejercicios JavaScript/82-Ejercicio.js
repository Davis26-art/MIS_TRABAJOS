// Suma, resta, multiplicación y división.

// Función para sumar dos números
function sumar(a, b) { 
    return a + b; // devuelve la suma
}

// Función para restar dos números
function restar(a, b) { 
    return a - b; // devuelve la resta
}

// Función para multiplicar dos números
function multiplicar(a, b) { 
    return a * b; // devuelve la multiplicación
}

// Función para dividir dos números
function dividir(a, b) { 
    return a / b; // devuelve la división
}

// Probamos las funciones
console.log("Suma:", sumar(5, 3));           // 5 + 3 = 8
console.log("Resta:", restar(5, 3));         // 5 - 3 = 2
console.log("Multiplicación:", multiplicar(5, 3)); // 5 * 3 = 15
console.log("División:", dividir(5, 3));     // 5 / 3 ≈ 1.6667
