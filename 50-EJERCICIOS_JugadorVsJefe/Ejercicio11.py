print("Vamos a contar los números pares del 2 al 20:")
contador_Par = 2  # Empezamos en 2, el primer número par

while contador_Par <= 20:  # Mientras el contador sea menor o igual a 20
    print("Número par:", contador_Par)
    contador_Par = contador_Par + 2  # Sumamos 2 para saltar a los siguientes pares

print("¡Terminamos de contar los números pares!")
print("Gracias por participar.")