# Calculadora de altura futura
mi_Altura = 1.65  # Altura actual en metros
crecimiento_Anual = 0.05  # Crecimiento por año en metros
años_Futuros = 4  # Años que pasarán

# Cálculo de altura futura
altura_Futura = mi_Altura + (crecimiento_Anual * años_Futuros)

# Mostrar resultados
print("Actualmente mido", mi_Altura, "metros")
print("Si crezco", crecimiento_Anual, "m por año, en", años_Futuros, "años mediré", altura_Futura, "metros")
