# Calculadora de perímetro
base = 5  # Ancho del rectángulo
altura = 3  # Alto del rectángulo

# Fórmula del perímetro: 2 * (base + altura)
perimetro = 2 * (base + altura)

# Mostrar resultados
print("Rectángulo de: ", base, "x", altura)
print("El perímetro es: ", perimetro, "unidades")
