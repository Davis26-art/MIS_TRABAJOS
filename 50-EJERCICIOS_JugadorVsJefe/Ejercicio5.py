# Calculadora de promedio de tiempos en una carrera
tiempo_vuelta1 = 65.2  # Tiempo en segundos de la primera vuelta
tiempo_vuelta2 = 62.8  # Tiempo en segundos de la segunda vuelta
tiempo_vuelta3 = 64.5  # Tiempo en segundos de la tercera vuelta

# Sumamos los tiempos
tiempo_total = tiempo_vuelta1 + tiempo_vuelta2 + tiempo_vuelta3

# Calculamos el promedio
promedio_tiempo = tiempo_total / 3

# Mostrar resultados
print("Tiempos por vuelta:", tiempo_vuelta1, tiempo_vuelta2, tiempo_vuelta3)
print("Promedio por vuelta:", promedio_tiempo, "segundos")
