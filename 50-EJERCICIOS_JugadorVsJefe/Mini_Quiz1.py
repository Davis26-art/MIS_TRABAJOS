#¿Cuál es la diferencia entre = y ==?
#A) No hay diferencia
#B) = asigna valor, == compara
#C) = compara, == asigna
#D) Solo se usan en diferentes lenguajes

# Respuesta correcta: B 
# Explicación: En Python (y muchos otros lenguajes), el símbolo '=' se utiliza para asignar un valor a una variable, mientras que '==' se utiliza para comparar dos valores y verificar si son iguales.