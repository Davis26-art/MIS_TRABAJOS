#¿Qué hace range(1, 5)?
#A) Genera números del 1 al 5
#B) Genera números del 1 al 4
#C) Genera números del 0 al 5
#D) Genera números del 0 al 4

# Respuesta correcta: B
# Explicación: En Python, range(1, 5) genera una secuencia de números que comienza en 1 y termina antes de 5, es decir, incluye 1, 2, 3 y 4.