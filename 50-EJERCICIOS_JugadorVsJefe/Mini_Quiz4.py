#¿Qué sucede si no incrementas la variable en un bucle
#while?
#A) El programa termina
#B) Se produce un error
#C) Se crea un bucle infinito
#D) Se ejecuta solo una vez

# Respuesta correcta: C
# Explicación: Si no incrementas la variable de control en un bucle while, el bucle nunca alcanzará la condición de terminación, lo que resultará en un bucle infinito.