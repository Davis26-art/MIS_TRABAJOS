// Ejercicio 13: Usar switch para imprimir el día de la semana basado en un número
let dia = 4;// Número del día de la semana (1-7)

switch (dia) {//  Evaluar el valor de dia
  case 1:
    console.log("Lunes");// Si dia es 1, imprimir "Lunes"
    break;

  case 2:
    console.log("Martes");// Si dia es 2, imprimir "Martes"
    break;

  case 3:
    console.log("Miercoles");// Si dia es 3, imprimir "Miercoles"
    break;

  case 4:
    console.log("Jueves");// Si dia es 4, imprimir "Jueves"
    break;

  case 5:
    console.log("Viernes");// Si dia es 5, imprimir "Viernes"
    break;

  case 6:
    console.log("Sabado");// Si dia es 6, imprimir "Sabado"
    break;

  case 7:
    console.log("Domingo");// Si dia es 7, imprimir "Domingo"
    break;
}
